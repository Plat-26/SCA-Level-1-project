    public RegistrationService(UserService userService, ConfirmationTokenService confirmationTokenService, EmailSender emailSender, EmailValidator emailValidator) {
        this.userService = userService;
        this.confirmationTokenService = confirmationTokenService;
        this.emailSender = emailSender;
        this.emailValidator = emailValidator;
    }

    @Transactional
    public String register(RegistrationRequestDTO requestDTO) {
        boolean isValidEmail = emailValidator.test(requestDTO.getEmail());

        if(!isValidEmail) {
            throw new FailedRegistrationException(String.format("The provided email %s is invalid", requestDTO.getEmail()));
        }
        String token = userService.signUpUser(new User(
                requestDTO.getFirstName(),
                requestDTO.getLastName(),
                requestDTO.getEmail(),
                requestDTO.getPassword(),
                UserRole.USER
        ));
        String link = "http://localhost:8080/users/register/confirm?token=" + token;
        emailSender.send(requestDTO.getEmail(), buildEmail(requestDTO.getFirstName(), link));
        return token;
    }

    @Transactional
    public String confirmToken(String token) {
        ConfirmationToken confirmationToken = confirmationTokenService.getToken(token);

        if(confirmationToken.getConfirmedAt() != null) {
            throw new FailedRegistrationException("Email already confirmed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if(expiredAt.isBefore(LocalDateTime.now())) {

            User user = confirmationToken.getUser();
            String newToken = confirmationTokenService.createConfirmationToken(user);
            String link = "http://localhost:8080/users/register/confirm?token=" + newToken;
            emailSender.send(user.getEmail(), buildEmail(user.getFirstName(), link));

            throw new FailedRegistrationException("Token expired, please confirm new token sent to email");
        }

        confirmationTokenService.setConfirmedAt(token);
        userService.enableAppUser(confirmationToken.getUser().getEmail());
        return "confirmed";
    }

    @Transactional
    public String handleForgotPassword(String email) {
        User user = userService.getUserByName(email);
        String resetPasswordToken = UUID.randomUUID().toString();

        ConfirmationToken token = new ConfirmationToken(
                resetPasswordToken,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                user
        );
        confirmationTokenService.saveConfirmationToken(token);

        String link = "http://localhost:8080/users/register/reset-password?token=" + resetPasswordToken;
        emailSender.send(email, buildResetPasswordEmail(user.getFirstName(), link));
        return "Check email to reset password";
    }

    public String resetPassword(String token, ResetPasswordDTO resetPasswordDTO) {
        ConfirmationToken userToken = confirmationTokenService.getToken(token);

        if(userToken.getExpiresAt().isBefore(LocalDateTime.now())) {
            return "expired token please try again";
        }

        return userService.updatePassword(userToken.getUser(), resetPasswordDTO.getPassword());
    }


